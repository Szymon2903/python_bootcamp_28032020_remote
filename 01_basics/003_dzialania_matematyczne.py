# Wykorzystując zmienne i działania na liczbach
# 1. Policz pole trójkąta o podstawie 10 i wysokości 5
a = 10
h = 5

triangle_area = a * h / 2

print("Pole trójkąta", triangle_area)

# 2. pole koła o promieniu 7
r = 7
pi = 3.14

circle_area = pi * r ** 2

print("Pole koła", circle_area)

# 3. pole trapezu o długości podstaw 3 i 9 oraz h=6.5
a = 3
b = 9
h = 6.5

trapeze_area = (a + b) * h / 2

print("Pole trapezu", trapeze_area)

# 4. Objętość kuli o promieniu 7/8 
r = 7 / 8
sphere_volume = (4 / 3) * pi * r ** 3

print("Objętośc kuli", sphere_volume)
